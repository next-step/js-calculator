Cypress.Commands.add('clickNumbers', (numbers) => {
  numbers.split('').forEach((value) => {
    cy.contains('button', value).click();
  });
});

Cypress.Commands.add('judgeResult', (result) => {
  cy.contains('button', '=').click();
  cy.get('#total').should('have.text', result);
});
