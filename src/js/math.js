/**
 * @param {number} augend 피가수
 * @param {number} addend 가수
 * @returns {number} 결과값
 */
export const add = (augend, addend) => augend + addend;

/**
 * @param {number} minuend 피감수
 * @param {number} subtrahend 감수
 * @returns {number} 결과값
 */
export const subtract = (minuend, subtrahend) => minuend - subtrahend;

/**
 * @param {number} multiplier 승수
 * @param {number} multiplicand 피승수
 * @returns {number} 결과값
 */
export const multiply = (multiplier, multiplicand) => multiplier * multiplicand;

/**
 * @param {number} dividend 피제수
 * @param {number} divisor 제수
 * @returns {number} 결과값
 */
export const divide = (dividend, divisor) => dividend / divisor;
