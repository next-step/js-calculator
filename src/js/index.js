class Calculator {
  clear() {
    return 0;
  }
  add(num1, num2) {
    return num1 + num2;
  }
  subtract(num1, num2) {
    return num1 - num2;
  }
  multiply(num1, num2) {
    return num1 * num2;
  }
  division(num1, num2) {
    return Math.floor(num1 / num2);
  }
}


export default Calculator;

